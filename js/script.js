const add = function (a, b) {
    let total = 0;
    total = a + b;
    return total;
};

const subtract = function (a, b) {
    let total = 0;
    total = a - b;
    return total;
}

const multiply = function (a, b) {
    let total = 1;
    total = a * b;
    return total;
}

const divide = function (a, b) {
    let total = 1;
    total = a / b;
    return total;
}

